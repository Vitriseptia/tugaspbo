public class Driver extends Gojek{
    private String namdriv;
    private String no;
    private int hp;
    private String type;

    public String getNamdriv() {
        return namdriv;
    }

    public void setNamdriv(String namdriv) {
        this.namdriv = namdriv;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
